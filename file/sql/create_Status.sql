CREATE TABLE `Status` (
  `id` bigint(20) NOT NULL,
  `createdAt` datetime NOT NULL,
  `idstr` varchar(1024) NOT NULL,
  `userId` bigint(20) NOT NULL,
  `text` varchar(1024) DEFAULT NULL,
  `source` varchar(10240) DEFAULT NULL,
  `favorited` varchar(8) DEFAULT NULL,
  `truncated` varchar(8) DEFAULT NULL,
  `inReplyToStatusId` bigint(20) DEFAULT NULL,
  `inReplyToUserId` bigint(20) DEFAULT NULL,
  `inReplyToScreenName` varchar(1024) DEFAULT NULL,
  `thumbnailPic` varchar(1024) DEFAULT NULL,
  `bmiddlePic` varchar(1024) DEFAULT NULL,
  `originalPic` varchar(1024) DEFAULT NULL,
  `retweetedStatus` bigint(20) DEFAULT NULL,
  `geo` varchar(1024) DEFAULT NULL,
  `latitude` smallint(6) DEFAULT NULL,
  `longitude` smallint(6) DEFAULT NULL,
  `repostsCount` int(11) DEFAULT NULL,
  `commentsCount` int(11) DEFAULT NULL,
  `mid` varchar(1024) DEFAULT NULL,
  `annotations` varchar(1024) DEFAULT NULL,
  `mlevel` int(11) DEFAULT NULL,
  `visible` varchar(1024) DEFAULT NULL,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8